#ifndef distribution_Gen_H
#define distribution_Gen_H

#include <stdlib.h>

typedef struct s_uniformDistMetaData uniformDistMetaData; // data type for storing random-number generator and its current state

/*
	uniformDistInit returns a random-number generator which follows uniform distrribution
	input: int (minimum number), int (maximum number), int (common difference)
	output: uniformDistMetaData* (pointer to a random number generator)
	effects: NONE
*/
uniformDistMetaData* uniformDistInit(size_t min, size_t max, size_t step);

/*
	next returns next random-number generated by random-number generator
	input: uniformDistMetaData* (pointer to a random-number generator)
	output: int (random number)
	effects: NONE
*/
int next(uniformDistMetaData* uniformDist);

#endif